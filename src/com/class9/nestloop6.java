package com.class9;

public class nestloop6 {
	public static void main(String[] args) {
		
	
	for ( int i=1; i<=5; i++) {
		
		for ( int j=1; j<=i; j++) {
			System.out.print(j);
		}
			System.out.println();
	}
	
	System.out.println("*********");
	
	for ( int a=1; a<10; a++) {
		
		for ( int b=1; b<=a; b++) {
			System.out.print(a);
		} System.out.println();
	}
	
	System.out.println("&&&&&&&&&&&&&&&&&&&&&&&&&&&&&&&&&&&&&&&&&&&&&&&");
	


}}
